tableHolder.onclick = function(e) {

  var target = e.target;

  if(target == e.target.closest('th')) {

    Sorting(target.textContent, target.cellIndex);

	}else if(target == e.target.closest('.pageList')) {

    var bs = navPanel.getElementsByTagName('button');

    for(var i = 0; i < bs.length; i++) {

      if(bs[i].textContent == currentPage) {

        bs[i].classList.add('inactiveButton');

      }

    }

    target.classList.remove('inactiveButton');
    currentPage = target.value;

    checkPage();

    buttonPrep(currentPage);

  }else if(target == e.target.closest('.search')) {

    filterTable(searchInput.value);

  }else if(target == e.target.closest('.activeDiv')) {

    bs = navPanel.getElementsByTagName('button');

    for(i = 0; i < bs.length; i++) {

      if(bs[i].textContent == currentPage) {

        bs[i].classList.add('inactiveButton');
        if(target == next) {
          bs[i - 1].classList.remove('inactiveButton');
          currentPage++;
        }else if(target == previous) {
          bs[i + 1].classList.remove('inactiveButton');
          currentPage--;
          break;
        } 
      }
    }
    checkPage();
    buttonPrep(currentPage);
  }

  function filterTable(keyWord) {

    var tempbody = table.getElementsByTagName('tbody')[0];
    var rowsArray = [].slice.call(tbody.rows, 1);
    var newArray = rowsArray.filter(function(row) {

      for(var i = 0; i < row.cells.length; i++) {

        if(row.cells[i].textContent == keyWord) {

          return row;

        }

      }

    });
    if(keyWord == '') {

      table.removeChild(tempbody);
      table.appendChild(tbody);
      updateButton(rowsArray.length);

    }
    if(newArray.length == 0) {

      return;

    }
    updateTable(tempbody, newArray, 1);

  }

  function checkPage() {
    if(currentPage == 6) {
      next.classList.remove('activeDiv');
      next.classList.add('inactiveDiv');
      previous.classList.add('activeDiv');
      previous.classList.remove('inactiveDiv');
    }else if(currentPage == 1) {
      previous.classList.remove('activeDiv');
      previous.classList.add('inactiveDiv');
      next.classList.add('activeDiv');
      next.classList.remove('inactiveDiv');
    }else{
      next.classList.add('activeDiv');
      next.classList.remove('inactiveDiv');
      previous.classList.add('activeDiv');
      previous.classList.remove('inactiveDiv');
    }

  }

  function Sorting(name, number) {

    var tbody = table.getElementsByTagName('tbody')[0];
    var rowsArray = [].slice.call(tbody.rows, 1);
    var compare;

    if( (!target.classList.contains('upSort') && !target.classList.contains('downSort')) || 
          target.classList.contains('downSort') ) {

      target.classList.toggle('upSort');
      target.classList.remove('downSort');

    }else if(target.classList.contains('upSort')) {

      target.classList.remove('upSort');
      target.classList.add('downSort');

    }
    
    compare = function(a, b) {

        a = a.cells[number].textContent;
        b = b.cells[number].textContent;
        var reg;
        switch (name) {
            
            case 'Start date':
                reg = /(\d+)\/(\d+)\/(\d+)/;
                a = +a.match(reg).splice(1, 3).join('');
                b = +b.match(reg).splice(1, 3).join('');
                break;
            case 'Age':
                a = +a;
                b = +b;
                break;
            case 'Salary':
                reg = /\$(\d+),(\d+)/;
                a = +a.match(reg).splice(1, 2).join('');
                b = +b.match(reg).splice(1, 2).join('');
            break;

        } 
        if(target.classList.contains('downSort')) {

          var temp = a;
          a = b;
          b = temp;

        }
        if(a < b) {

            return -1;

        }else if(a > b) {

            return 1;

        }
        return 0;

    };

    rowsArray.sort(compare);
    table.removeChild(tbody);

    for (var i = 0; i < rowsArray.length; i++) {

      tbody.appendChild(rowsArray[i]);

    }
    table.appendChild(tbody);
    buttonPrep(currentPage);
    
  }

};

function updateTable(oldBody, newArray, startPage) {

  table.removeChild(oldBody);
  var newBody = document.createElement('tbody');
  newBody.appendChild(trh.cloneNode(true));
  for (var i = 0; i < newArray.length; i++) {

    newBody.appendChild(newArray[i].cloneNode(true));

  }
  table.appendChild(newBody);
  updateButton(newArray.length);
  buttonPrep(startPage);

}

function updateButton(rows) {

  var buttons = navPanel.getElementsByTagName('button');
  for(var i = 0; i < buttons.length; i++) {

    navPanel.removeChild(buttons[i--]);

  }
  navPanel.appendChild(next);
  numberOfPages = Math.ceil( rows / 10 );
  for(i = numberOfPages; i > 0; i--) {

    var button = document.createElement('button');
    button.value = button.textContent = i;
    button.classList.add('pageList');
    button.classList.add('inactiveButton');
    if(button.textContent == currentPage) {

      button.classList.remove('inactiveButton');

    }
    navPanel.appendChild(button);
    
  }
  navPanel.appendChild(previous);

}

function buttonPrep(currentPage) {

  var multiplier = currentPage * 10;
  var counter = 0;
  var arr = [];
  for(var i = multiplier - 10; i < multiplier; i++) {

    arr[counter++] = i;

  }
  showTable(arr);
  
}

function showTable(pageNumber) {

	var tbody = table.getElementsByTagName('tbody')[0];
  var rowsArray = [].slice.call(tbody.rows, 1);
  var end = pageNumber[pageNumber.length - 1];
  var start = pageNumber[0]; 
  var  counter = 0;
	
	for(var i = 0; i < start; i++) {

    rowsArray[i].classList.add('invis');
    
  }
  for(; i <= end; i++) {

    if(rowsArray[i].rowIndex - 1 == pageNumber[counter]) {

      rowsArray[i].classList.remove('invis');
      counter++;

    }else{

      rowsArray[i].classList.add('invis');

    }

  }
  for(; i < rowsArray.length; i++) {

    rowsArray[i].classList.add('invis');
    
  }

}